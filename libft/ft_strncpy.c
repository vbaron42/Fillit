#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char	*d;

	d = dest;
	while (n--)
	{
		if (*src != '\0')
			*d++ = *src++;
		else
			*d++ = '\0';
	}
	return (dest);
}
